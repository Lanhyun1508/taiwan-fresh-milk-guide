async function importData() {
  console.log("Connecting to database...");
  
  const connection = await mysql.createConnection(process.env.DATABASE_URL);
  
  console.log(`Importing ${oldBrandsData.length} brands from old website...`);
  
  let successCount = 0;
  let skipCount = 0;
  let errorCount = 0;
  
  for (const brand of oldBrandsData) {
    try {
      // 檢查是否已存在相同品牌和產品
      const [existing] = await connection.execute(
        `SELECT id FROM milk_brands WHERE brandName = ? AND productName = ?`,
        [brand.brandName, brand.productName]
      );
      
      if (existing.length > 0) {
        console.log(`⊙ Skipped (already exists): ${brand.brandName} - ${brand.productName}`);
        skipCount++;
        continue;
      }
      
      await connection.execute(
        `INSERT INTO milk_brands (brandName, productName, pasteurizationType, volume, shelfLife, price, origin, ingredients, officialWebsite, physicalChannels, onlineChannels, isOrganic, isImported, notes) 
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        [
          brand.brandName,
          brand.productName,
          brand.pasteurizationType,
          brand.volume,
          brand.shelfLife,
          brand.price,
          brand.origin,
          brand.ingredients,
          brand.officialWebsite,
          brand.physicalChannels,
          brand.onlineChannels,
          brand.isOrganic,
          brand.isImported,
          brand.notes,
        ]
      );
      console.log(`✓ Imported: ${brand.brandName} - ${brand.productName}`);
      successCount++;
    } catch (error) {
      console.error(`✗ Failed to import ${brand.brandName}:`, error.message);
      errorCount++;
    }
  }
  
  await connection.end();
  
  console.log("\n=== Import Summary ===");
  console.log(`Total brands: ${oldBrandsData.length}`);
  console.log(`Successfully imported: ${successCount}`);
  console.log(`Skipped (already exists): ${skipCount}`);
  console.log(`Errors: ${errorCount}`);
  console.log("Import complete!");
  
  process.exit(0);
}

importData().catch((error) => {
  console.error("Import failed:", error);
  process.exit(1);
});
